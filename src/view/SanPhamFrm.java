    /**
     * Thêm mới sản phẩm
     *
     * @param evt
     */
    private void btnThemMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemMoiActionPerformed
        SanPham item = new SanPham();

        item.setMaSP(txtMaSP.getText());
        item.setTenSP(txtTenSP.getText());
        item.setDonGia(Float.parseFloat(txtDonGia.getText()));
        item.setLoaiSP(txtLoaiHang.getText());
        item.setDonViTinh(txtDonViTinh.getText());

        items.add(item);
        if (new SanPhamDAO().addItem(item)) {
            showTable();
            resetForm();
            JOptionPane.showMessageDialog(this, "Thêm mới thành công");
        } else {
            JOptionPane.showMessageDialog(this, "Phát sinh lỗi");
        }


    }//GEN-LAST:event_btnThemMoiActionPerformed

    /**
     * Xóa sán phẩm
     *
     * @param evt
     */
    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int indexRow = tblSanPham.getSelectedRow(); // lấy vị trí hàng đang được chọn
        int isDelete = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa sản phẩm này không?");
        // 0: có, 1: đóng
        if (isDelete == 0) {
            if (new SanPhamDAO().deleteItem(items.get(indexRow))) {
                items.remove(indexRow); // xóa phần tử khỏi mảng items
                showTable(); // hiển thị lại bảng
                resetForm();
            } else {
                JOptionPane.showMessageDialog(this, "Phát sinh lỗi");
            }

        }
    }//GEN-LAST:event_btnXoaActionPerformed
    /**
     * Lấy dữ liệu của hàng trc khi cập nhật
     *
     * @param evt
     */
    private void tblSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamMouseClicked
        int indexRow = tblSanPham.getSelectedRow(); // lấy vị trí hàng đang được chọn
        SanPham item = items.get(indexRow);
        
        txtMaSP.setEnabled(false);
        
        txtMaSP.setText(item.getMaSP());
        txtTenSP.setText(item.getTenSP());
        txtDonGia.setText("" + item.getDonGia());
        txtDonViTinh.setText(item.getDonViTinh());
        txtLoaiHang.setText(item.getLoaiSP());
    }//GEN-LAST:event_tblSanPhamMouseClicked
    /**
     * Cập nhật sản phẩm
     *
     * @param evt
     */
    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        int indexRow = tblSanPham.getSelectedRow(); // lấy vị trí hàng đang được chọn
        SanPham item = new SanPham();

        item.setMaSP(txtMaSP.getText());
        item.setTenSP(txtTenSP.getText());
        item.setDonGia(Float.parseFloat(txtDonGia.getText()));
        item.setLoaiSP(txtLoaiHang.getText());
        item.setDonViTinh(txtDonViTinh.getText());

        if (new SanPhamDAO().updateItem(items.get(indexRow))) {
            items.remove(indexRow);
            items.add(item);
            showTable();
            resetForm();
            JOptionPane.showMessageDialog(this, "Cập nhật thành công");
        } else {
            JOptionPane.showMessageDialog(this, "Có lỗi");
        }


    }//GEN-LAST:event_btnCapNhatActionPerformed

