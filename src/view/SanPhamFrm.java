    /**
     * Hiển thị bảng dữ liệu
     */
    public void showTable() {
        model.setRowCount(0); // xóa dữ liệu ở bảng cũ đi
        for (SanPham item : items) {
            model.addRow(new Object[]{
                model.getRowCount() + 1, item.getMaSP(), item.getTenSP(), item.getDonGia(), item.getLoaiSP(), item.getDonViTinh()
            });
        }
    }

