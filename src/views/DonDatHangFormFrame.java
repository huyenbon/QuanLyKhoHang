    public void layPhanTuDuocChon(DonDatHang ptu) {
        isCapNhat = true;

        txtMaDonHang.setText(ptu.getMaDonDH());
        txtMaDonHang.setEnabled(false);
        txtNgayDatHang.setText("" + new SimpleDateFormat("dd/MM/yyyy").format(ptu.getNgayDH()));
        cbNhaCungCap.setSelectedItem(ptu.getTenNhaCC());
    }
    
    /**
     * Tạo combobox tên nhà cung cấp
     */
    public void thietLapTenNhaCungCap() {
        dsNhaCungCap = new NhaCungCapDao().layDanhSachPhanTu();
        dsTenNhaCungCap = new String[dsNhaCungCap.size()];

        for (int i = 0; i < dsNhaCungCap.size(); i++) {
            dsTenNhaCungCap[i] = dsNhaCungCap.get(i).getTenNhaCC();
        }
        cbNhaCungCap.setModel(new javax.swing.DefaultComboBoxModel(dsTenNhaCungCap));
    }

