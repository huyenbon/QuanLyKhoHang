    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        String maDonDatHang = null, maNhaCC = null;
        Date ngayDH = null;
        boolean isDone = true;

        if (txtMaDonHang.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Mã đơn đặt hàng không được để trống");
            isDone = false;
        } else {
            maDonDatHang = txtMaDonHang.getText();
        }
        
        try {
            ngayDH = new SimpleDateFormat("dd/MM/yyyy").parse(txtNgayDatHang.getText());
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Ngày đặt hàng không đúng định dạng");
            isDone = false;
        }

        int viTriNhaCungCap = cbNhaCungCap.getSelectedIndex();
        maNhaCC = dsNhaCungCap.get(viTriNhaCungCap).getMaNhaCC();
        
        if (isDone) {
            DonDatHang ptu = new DonDatHang(maDonDatHang, ngayDH, maNhaCC);
            if (!isCapNhat) {
                if (new DonDatHangDao().themPhanTu(ptu)) {
                    JOptionPane.showMessageDialog(this, "Thêm mới thành công!");
                    home.hienThiDanhSach();
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Mã nhân viên đã tồn tại!");
                }
            } else {
                if (new DonDatHangDao().capNhatPhanTu(ptu)) {
                    JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
                    home.hienThiDanhSach();
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Xảy ra lỗi!");
                }
            }
            home.thayDoiTrangThaiButton(true, false, false, false);
        }
    }//GEN-LAST:event_btnLuuActionPerformed

