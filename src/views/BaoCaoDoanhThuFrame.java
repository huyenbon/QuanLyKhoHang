    /**
     * Hiển thị các hàng của bảng
     */
    public void hienThiDanhSach(int ngay, int thang, int nam) {
        danhsach = new DonXuatDao().thongKeBaoCao(ngay, thang, nam);
        model.setRowCount(0);
        for (DonXuat ptu : danhsach) {
            model.addRow(new Object[]{
                model.getRowCount() + 1, ptu.getMaDonXuat(), df.format(ptu.getTongTien())
            });
        }
    }

