    private void btnThongKeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThongKeActionPerformed
        int ngay = cbNgay.getSelectedIndex() + 1;
        int thang = cbThang.getSelectedIndex() + 1;
        int nam;
        if ("".equals(txtNam.getText())) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập năm cần thống kê");
        } else {
            try {
                nam = Integer.parseInt(txtNam.getText());
                hienThiDanhSach(ngay, thang, nam);
                if (danhsach.size() == 0) {
                    JOptionPane.showMessageDialog(this, "Danh sách thống kê rỗng");
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Năm không đúng định dạng");
            }
        }
    }//GEN-LAST:event_btnThongKeActionPerformed

