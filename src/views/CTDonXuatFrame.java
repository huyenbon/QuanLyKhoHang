    private void btnThemMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemMoiActionPerformed
        int soLuong = 0;
        float donGia = 0;
        String maSanPham = null;
        boolean isDone = true;

        if ("".equals(txtSoLuong.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Số lượng không được để trống");
            isDone = false;
        } else {
            try {
                soLuong = Integer.parseInt(txtSoLuong.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(rootPane, "Số lượng không đúng định dạng");
                isDone = false;
            }
        }
        
        if ("".equals(txtDonGia.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Đơn giá không được để trống");
            isDone = false;
        } else {
            try {
                donGia = Float.parseFloat(txtDonGia.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(rootPane, "Đơn giá không đúng định dạng");
                isDone = false;
            }
        }
        
        int viTriSanPham = cbSanPham.getSelectedIndex();
        maSanPham = dsSanPham.get(viTriSanPham).getMaSanPham();
        
        if(isDone) {
            CTDonXuat ptu = new CTDonXuat(maDonXuat, maSanPham, soLuong, donGia);
            if(new CTDonXuatDao().themPhanTu(ptu)) {
                JOptionPane.showMessageDialog(rootPane, "Thêm mới thành công");
                hienThi();
                lamMoiForm();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Sản phẩm đã tồn tại");
            }
        }
    }//GEN-LAST:event_btnThemMoiActionPerformed

    private void tblDanhSachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDanhSachMouseClicked
        viTri = tblDanhSach.getSelectedRow();
        
        if(viTri > -1) {
            btnCapNhat.setEnabled(true);
            btnXoa.setEnabled(true);
            btnThemMoi.setEnabled(false);
            
            CTDonXuat ptu = dsCTDonXuat.get(viTri);
            int index = 0;
            for (int i = 0; i < dsSanPham.size(); i++) {
                if(ptu.getMaSanPham().equals(dsSanPham.get(i).getMaSanPham())) {
                    index = i;
                    break;
                }
            }
            cbSanPham.setSelectedIndex(index);
            cbSanPham.setEnabled(false);
            txtSoLuong.setText("" + ptu.getSoLuongXuat());
            txtDonGia.setText("" + ptu.getDonGia());
        } else {
            btnCapNhat.setEnabled(false);
            btnXoa.setEnabled(false);
            btnThemMoi.setEnabled(true);
            lamMoiForm();
        }
    }//GEN-LAST:event_tblDanhSachMouseClicked

