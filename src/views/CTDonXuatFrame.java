    /**
     * Thiết lập combobox tên sản phẩm
     */
    public void thietLapTenSanPham() {
        dsSanPham = new SanPhamDao().layDanhSachPhanTu();
        dsTenSanPham = new String[dsSanPham.size()];

        for (int i = 0; i < dsSanPham.size(); i++) {
            dsTenSanPham[i] = dsSanPham.get(i).getTenSanPham();
        }
        cbSanPham.setModel(new javax.swing.DefaultComboBoxModel(dsTenSanPham));
    }

    /**
     * Lấy thông tin nhận được
     * @param maNhanDuoc 
     */
    public void layThongTinNhanDuoc(String maNhanDuoc) {
        maDonXuat = maNhanDuoc;
        hienThi();
    }
    
    /**
     * Hiển thị bảng dữ liệu
     */
    public void hienThi() {
        dsCTDonXuat = new CTDonXuatDao().layDanhSachPhanTu(maDonXuat);
        model.setRowCount(0);
        for (CTDonXuat ptu : dsCTDonXuat) {
            model.addRow(new Object[] {
                model.getRowCount() + 1, ptu.getMaSanPham(), ptu.getTenSanPham(), ptu.getSoLuongXuat()
            });
        }
    }
    
